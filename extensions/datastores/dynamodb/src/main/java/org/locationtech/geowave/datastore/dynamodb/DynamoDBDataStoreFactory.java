package org.locationtech.geowave.datastore.dynamodb;

import org.locationtech.geowave.core.store.BaseDataStoreFactory;
import org.locationtech.geowave.core.store.StoreFactoryHelper;
import org.locationtech.geowave.core.store.StoreFactoryOptions;
import org.locationtech.geowave.core.store.api.DataStore;
import org.locationtech.geowave.core.store.operations.DataStoreOperations;
import org.locationtech.geowave.datastore.dynamodb.config.DynamoDBOptions;
import org.locationtech.geowave.datastore.dynamodb.operations.DynamoDBOperations;

public class DynamoDBDataStoreFactory extends BaseDataStoreFactory {
  public DynamoDBDataStoreFactory(
      final String typeName,
      final String description,
      final StoreFactoryHelper helper) {
    super(typeName, description, helper);
  }

  @Override
  public DataStore createStore(final StoreFactoryOptions options) {
    if (!(options instanceof DynamoDBOptions)) {
      throw new AssertionError("Expected " + DynamoDBOptions.class.getSimpleName());
    }
    final DynamoDBOptions opts = (DynamoDBOptions) options;

    final DataStoreOperations dynamodbOperations = helper.createOperations(opts);

    return new DynamoDBDataStore((DynamoDBOperations) dynamodbOperations);
  }
}
